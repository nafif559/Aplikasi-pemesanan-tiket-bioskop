    private void btnLanjutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLanjutActionPerformed
        // TODO add your handling code here:
        String strJumlah = txtJumlahTiket.getText().trim();

    if (strJumlah.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan jumlah tiket dulu!");
        return;
    }

    int jumlah;
    try {
        jumlah = Integer.parseInt(strJumlah);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Jumlah tiket harus angka!");
        return;
    }

    // Ambil sisa kursi untuk jadwal ini
    int sisaKursi = getSisaKursi(Session.idJadwal);

    if (jumlah < 1 || jumlah > sisaKursi) {
        JOptionPane.showMessageDialog(this, "Jumlah tiket harus antara 1 - " + sisaKursi);
        return;
    }

    // simpan ke Session
    Session.jumlahTiket = jumlah;

    // buka Form Studio
    new Studio().setVisible(true);
    this.dispose();
    }//GEN-LAST:event_btnLanjutActionPerformed

