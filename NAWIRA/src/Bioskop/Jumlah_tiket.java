    private int getSisaKursi(int idJadwal) {
    int sisa = 0;
    try {
        Connection conn = Koneksi.getConnection();

        // 1. Ambil kapasitas studio
        String sql1 = "SELECT s.kapasitas FROM jadwal j JOIN studio s ON j.id_studio = s.id_studio WHERE j.id_jadwal = ?";
        PreparedStatement ps1 = conn.prepareStatement(sql1);
        ps1.setInt(1, idJadwal);
        ResultSet rs1 = ps1.executeQuery();
        if (rs1.next()) {
            sisa = rs1.getInt("kapasitas");
        }

        // 2. Kurangi tiket yang sudah dibooking
        String sql2 = "SELECT COUNT(*) AS terisi FROM tiket WHERE id_jadwal = ?";
        PreparedStatement ps2 = conn.prepareStatement(sql2);
        ps2.setInt(1, idJadwal);
        ResultSet rs2 = ps2.executeQuery();
        if (rs2.next()) {
            sisa -= rs2.getInt("terisi");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal ambil sisa kursi: " + e.getMessage());
    }
    return sisa;
}




